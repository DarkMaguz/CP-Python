# Function for printing out the sum of two numbers
# 
# @param num1: The first number to be added
# @param num2: The second number to be added
# 
# example useage:
#   print_sum(1, 2) => 3
# 
def print_sum(num1, num2):
  print(num1 + num2)

# Function for printing out the difference of two numbers'
# 
# @param num1: The first number to be subtracted
# @param num2: The second number to be subtracted
# 
# example useage:
#   print_difference(1, 2) => 1
#
def print_difference(num1, num2):
  print(num1 - num2)


# Function for printing out the product of two numbers
# 
# @param num1: The first number to be multiplied
# @param num2: The second number to be multiplied
#
# example useage:
#   print_product(1, 2) => 2
#
def print_product(num1, num2):
  print(num1 * num2)
